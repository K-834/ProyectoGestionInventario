    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String usuario = txtAdmin.getText();
        String pass = txtContrase単a.getText();
        DaoAdmin dao = new DaoImplAdmin();

        try {
            Usuario adm = dao.adminLogin(usuario, pass);
            Usuario admNam = dao.adminNombre(usuario, pass);
            if ((adm.getCodigo().equals(usuario) || adm.getContrase単a().equals(pass)) && adm.getNivelSeguridad() >= 2) {
                Memoria.put("autorizacion", admNam.getNombre());
                Memoria.put("codAdmin", adm.getCodigo());
                Editar.main(null);
                this.dispose();
            } else if (adm.getNivelSeguridad() == 1) {
                JOptionPane.showMessageDialog(null, "TU NIVEL DE AUTORIZACION ES MINIMA PARA ENTRAR ",
                        "Nivel de Autorizacion",
                        JOptionPane.INFORMATION_MESSAGE);
                txtAdmin.setText("");
                txtContrase単a.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "DATOS INCORRECTOS ",
                    "ERROR ",
                    JOptionPane.ERROR_MESSAGE);

        }

    }//GEN-LAST:event_jButton1ActionPerformed

